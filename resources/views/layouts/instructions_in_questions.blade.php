<h1>General Instructions</h1>
<ol>
	<li>The clock will be set at the server. The countdown timer at the right corner of the screen will display the remaining time available for you to complete the examination. When the timer reaches zero the examination ends by itself. You need to terminate the examination or submit the paper.</li>
	<li>The question palette displayed on the right side of the screen will show the status of each question using one of the following color:</li>

		White:
		You have not visited the question yet.

		Red:
		You have not answered the question.

		Green:
		You have answered the question.

		Violet:
		Marked the question for review.

	<li>The Marked for Review status for a question simply indicates that you would like to look at that question again. If a question is answered, but marked for review, then the answer to that question will be considered in the evaluation, unless the status is modified by the candidate.</li>
	<b style="color: red;"><h3>Navigating to a Question</h3></b>
	<li>To answer a question, do the following:</li>
		<ul>
			<li>Click on the question number in the Question Palette to go to that question directly.</li>
			<li>Select an answer for a multiple choice type question by clicking on bubble placed before the 4 choices A, B, C, D. Use the virtual numeric keypad to enter a number as an answer to a numerical type question.</li>
			<li>Click on Save and Next to save your answer for the current question and then go to the next question.</li>
			<li>Click on Mark for Review and Next to save your answer for the current question, mark it for review, and then go to the next question.</li>
			<li>Caution: Note that your answer for the current question will not be saved, if you navigate to another question directly by clicking on its question number.</li>
		</ul>

	<li>You can view all the questions by clicking on the Question Paper button. This feature is provided so that you can see the entire question paper at a glance. Note that the options for multiple choice type questions will not be shown.</li>
	<b style="color: red;"><h3>Answering a Question</h3></b>
	<li>Procedure for answering a multiple choice type question:</li>
		<ul>
			<li>To select your answer, click on the button of one of the options A, B, C, D</li>
			<li>To deselect your chosen answer, click on the button of the chosen option again or click on the Clear Response button</li>
			<li>To change your chosen answer, click on the button of another option</li>
			<li>To save your answer, you MUST click on the Save & Next button</li>
			<li>To mark the question for review, click on the Mark for Review & Next button. If an answer is selected for a question that is Marked for Review, that answer will be considered in the evaluation.</li>
		</ul>
	<li>Procedure for answering a numerical answer type question:</li>
		a. To enter a number as your answer, use the virtual numerical keypad
		b. In Numerical Type Questions, you are limited to answer till two decimal points.
		c. A fraction (eg. -0.3 or -.3) can be entered as an answer with or without "0" before the decimal point
		d. To clear your answer, click on the Clear Response button
			a.To save your answer, you MUST click on the Save & Next button
			b. To mark the question for review, click on the Mark for Review and Next button. If an answer is entered for a question that is Marked for Review, that answer will be considered in the evaluation.
			c. To change your answer to a question that has already been answered, first select that question for answering and then follow the procedure for answering that type of question.
			d. Note that ONLY Questions for which answers are saved or MARKED FOR REVIEW after answering will be considered for evaluation


	<li> Sections</li>
		a. Sections in the question paper will be displayed in the top bar of the screen. Questions in a section can be viewed by clicking on the name of that Section. The Section you are currently viewing will be highlighted
		b. After clicking on Save & the Next button for the last question in a Section, you will be automatically taken to the first question of the next Section in a sequence.
		c. You can move the mouse cursor over the name of the Section to view the answering status of that Section.
		d. You can shuffle between different Sections or change the optional Sections any number of times.


	<li> Calculators</li>
