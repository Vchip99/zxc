<h1>General Instructions</h1>
<ol>
	<li>The clock will be set at the server. The countdown timer at the right corner of the screen will display the remaining time available for you to complete the examination. When the timer reaches zero the examination ends by itself.</li>
	<li>The question palette displayed on the right side of the screen will show the status of each question using one of the following color:</li>

		SkyBlue:
		You have not visited the question yet.

		Red:
		You have not answered the question.

		Green:
		You have answered the question.

		Violet:
		Marked the question for review.

	<li>The Marked for Review status for a question simply indicates that you would like to look at that question again. If a question is answered, but marked for review, then the answer to that question will be considered in the evaluation, unless the status is modified by the candidate.</li>
